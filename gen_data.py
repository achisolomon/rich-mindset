J = lambda s: json.dumps(s, ensure_ascii=False)

# ---- HTML block ----
html_lines = ["const CATS = ["]
for name, items in DATA:
    html_lines.append(f" {{name:{J(name)}, items:[")
    for p, r in items:
        html_lines.append(f"   [{J(p)},{J(r)}],")
    html_lines.append(" ]},")
html_lines.append("];")
html_block = "\n".join(html_lines)

# ---- Python block ----
py_lines = ["cats_data=["]
for i,(name, items) in enumerate(DATA, start=1):
    py_lines.append(f' ({J(str(i)+". "+name)},[')
    for p, r in items:
        py_lines.append(f"   ({J(p)},{J(r)}),")
    py_lines.append(" ]),")
py_lines.append("]")
py_block = "\n".join(py_lines)

# inject into HTML
h = open("mindset-quiz.html",encoding="utf-8").read()
h2 = re.sub(r"const CATS = \[.*?\n\];", html_block, h, count=1, flags=re.DOTALL)
assert h2!=h, "HTML not replaced"
open("mindset-quiz.html","w",encoding="utf-8").write(h2)

# inject into build script
b = open("build_quiz4.py",encoding="utf-8").read()
b2 = re.sub(r"cats_data=\[.*?\n\]\n", py_block+"\n", b, count=1, flags=re.DOTALL)
assert b2!=b, "PY not replaced"
open("build_quiz4.py","w",encoding="utf-8").write(b2)

